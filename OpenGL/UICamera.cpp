#include "UICamera.h"
#include "TheShader.h"
#include "TheScreen.h"

//-------------------------------------------------------------------------------
//Constructor
//-------------------------------------------------------------------------------
UICamera::UICamera()
{
	TheScreen::Instance()->GetScreenSize(m_screenWidth, m_screenHeight);

	m_view = glm::mat4(1.0f);
}

//-------------------------------------------------------------------------------
//Destructor
//-------------------------------------------------------------------------------
UICamera::~UICamera()
{
}

//-------------------------------------------------------------------------------
//OnEnter
//-------------------------------------------------------------------------------
void UICamera::Create()
{
}

//-------------------------------------------------------------------------------
//Update
//-------------------------------------------------------------------------------
void UICamera::Update()
{
}

//-------------------------------------------------------------------------------
//Draw	
//-------------------------------------------------------------------------------
void UICamera::Draw()
{
	//----------------------------- Send view matrix to shaders

	TheShader::Instance()->SendUniformData("Lightless_view", 1, GL_FALSE, m_view);
}

//-------------------------------------------------------------------------------
//Destroy
//-------------------------------------------------------------------------------
void UICamera::Destroy()
{
}
