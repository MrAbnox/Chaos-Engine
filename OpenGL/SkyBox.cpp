#include "SkyBox.h"

//-------------------------------------------------------------------------------
//Constructor
//-------------------------------------------------------------------------------
SkyBox::SkyBox()
{
}

//-------------------------------------------------------------------------------
//Destructor
//-------------------------------------------------------------------------------
SkyBox::~SkyBox()
{
	Destroy();
}

//-------------------------------------------------------------------------------
//On Enter
//-------------------------------------------------------------------------------
void SkyBox::Create()
{
	v3_size = glm::vec3(1000.0f, 1000.0f, 1000.0f);

	bool isCubeMapped = true;
	box = new Cube(isCubeMapped, glm::vec3(1.0f, 1.0f, 1.0f), "Textures/Skybox/SkyBox_2.jpg", "SkyBox");
	box->Create("Lightless");

	box->Scale(v3_size);
}

//-------------------------------------------------------------------------------
//Update
//-------------------------------------------------------------------------------
void SkyBox::Update()
{
	box->Update();
}

//-------------------------------------------------------------------------------
//Draw
//-------------------------------------------------------------------------------
void SkyBox::Draw()
{
	box->Draw();
}

//-------------------------------------------------------------------------------
//Destroy
//-------------------------------------------------------------------------------
void SkyBox::Destroy()
{
}
